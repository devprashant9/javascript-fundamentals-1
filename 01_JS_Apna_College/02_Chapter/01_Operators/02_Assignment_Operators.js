/*
                Assignment Operators
                    =
                    +=
                    -=
                    *=
                    %=
                    **=

*/

let a = 2;
let b = 3;

a = a + b; //updates the value in variable a
console.log(a);

console.log(a += b); //short hand for a += b

console.log(a -= b);
console.log( a **= b);